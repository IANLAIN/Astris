function inject(lang, additionsStr) {
  const regex = new RegExp("(" + lang + ": \\{)([\\s\\S]*?)(    vacancies: \\[\\]?)");
  result = result.replace(regex, (match, p1, p2, p3) => {
    return p1 + p2 + additionsStr + p3;
  });
}

